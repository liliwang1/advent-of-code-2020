function countTrees(arr, right, down) {
    let tree = 0;
    arr.forEach((value, index) => {
        if (index % down === 0) {
            const rightMove = index / down * right + 1;
            if (rightMove % value.length === 0) {
                if (testIfIsTree(value, value.length - 1)) {
                    tree++;
                }
            }
            if (testIfIsTree(value, (rightMove % value.length) - 1)) {
                tree++;
            }
        }
    })
    console.log(tree);
    return tree;
}

